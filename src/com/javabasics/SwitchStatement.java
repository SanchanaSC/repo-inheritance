package com.javabasics;

public class SwitchStatement {

	public static void main(String[] args) {
		int choice=5;
		switch(choice) {
		case 1 : System.out.println("Idli");
				 break;
		case 2 : System.out.println("Wada");
				 break;
		case 3 :  System.out.println("Puri");
				 break;
		default : System.out.println("Upma");

		}
	}

}
